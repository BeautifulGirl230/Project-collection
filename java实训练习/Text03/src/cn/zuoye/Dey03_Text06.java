package cn.zuoye;

import java.util.Scanner;

public class Dey03_Text06 {

	public static void main(String[] args) {
		Scanner sr = new Scanner(System.in);
		System.out.println("请输入一个整数：");
		int x = sr.nextInt();
		if(x==1){
			System.out.println("x"+"="+x);
		}else if(x==5){
			System.out.println("x"+"="+x);
		}else if(x==10){
			System.out.println("x"+"="+x);
		}else{
			System.out.println("x"+"="+"none");
		}
	}

}
